"""Задача 1 (оператор if)
Напиши код, который проверяет, что переменная age больше 18, и выводит "Доступ разрешен"."""

age = 19
if age > 18:
    print("Доступ разрешен")

"""Задача 2 (оператор else)
Напиши код, который:

Проверяет, что переменная temperature меньше 0.

Если да — выводит "Лёд".

Если нет — выводит "Вода".

Пример переменной:

temperature = -5"""

temperature = -5
if temperature < 0:
    print("Лёд")
else:
    print("Вода")


"""Задача 3 (оператор elif)
Дана переменная:

time = 14  
Напиши код, который проверяет:

Если time < 12 — выводит "Утро",

Если 12 ≤ time < 18 — выводит "День",

Иначе — "Вечер"."""

time = 14
if time < 12:
    print("Утро")
elif 12 <= time < 18:
    print("День")
else:
    print("Вечер")

"""Задача 4 (логические операции)
Даны две переменные:

is_rainy = True  
is_windy = False  
Напиши условие, которое выводит "Зонт нужен", если:

Идёт дождь (is_rainy) ИЛИ ветрено (is_windy),

Но только если при этом is_windy не равен True (используй not).

Подсказка: Комбинируй or и not."""

is_rainy = True
is_windy = False

if not is_windy and (is_rainy or is_windy == True):
    print("Зонт нужен")

"""1. Оператор if
Напиши код, который проверяет, является ли число x = 10 положительным. Если да — выводи строку "Число положительное".

(Используй только if, без else/elif.)"""

x = 10
if x > 0:
    print("Число положительное")

"""2. Оператор else
Напиши код, который проверяет, чётное ли число y = 7. Если да — выводи "Чётное", иначе — "Нечётное".

(Используй if и else.)"""

y = 7
if y % 2 == 0:
    print("Чётное")
else:
    print("Нечётное")

"""3. Оператор elif
Дана переменная:

time = 14
Напиши код, который выведет:

"Утро" — если time от 5 до 11,

"День" — если от 12 до 17,

"Вечер" — если от 18 до 22,

"Ночь" — в остальных случаях."""

time = 14
if 5 <= time <= 11:
    print("Утро")
elif 12 <= time <= 17:
    print("День")
elif 18 <= time <= 22:
    print("Вечер")
else:
    print("Ночь")

"""4. Логические операции
Даны две переменные:

is_rainy = True
is_windy = False
Напиши код, который выведет "Возьми зонт", если идет дождь И нет ветра, иначе — "Сегодня без зонта"."""

is_rainy = True
is_windy = False

if is_rainy and not is_windy:
    print("Возьми зонт")
else:
    print("Сегодня без зонта")

"""5. Тернарный оператор
Дана переменная:

age = 18
Напиши одну строку кода, которая:

Проверит, что age >= 18,

Если да — выведет "Доступ разрешён",

Иначе — "Доступ запрещён".

(Используй тернарный оператор x if condition else y.)"""

age = 18
result = "Доступ разрешён" if age >= 18 else "Доступ запрещён"
print(result)

"""6. Логические операции в условиях
Даны три переменные:

has_ticket = True
has_passport = False
has_vip = True
Напиши код, который выведет "Проход разрешён", если:

У человека есть билет И паспорт (но если есть VIP-статус, паспорт не требуется).

Иначе — "Проход запрещён".

(Используй скобки для группировки условий.)"""

has_ticket = True
has_passport = False
has_vip = True

if (has_ticket and has_passport) or has_vip:
    print("Проход разрешён")
else:
    print("Проход запрещён")

"""7. Вложенные условия (if внутри if)
Даны переменные:

temperature = 25
is_summer = True
Напиши код, который выведет:

"Жарко" — если temperature > 30 летом (is_summer = True),

"Тепло" — если temperature > 20 (в любое время года),

"Прохладно" — в остальных случаях."""

temperature = 25
is_summer = True

if temperature > 30 and is_summer:
    print("Жарко")
elif temperature > 20:
    print("Тепло")
else:
    print("Прохладно")

"""8. Комбинированные условия
Даны переменные:

is_guest = False
is_admin = True
has_payment = True
Напиши код, который выведет "Доступ открыт", если:

Пользователь админ ИЛИ

Он не гость И есть оплата.

Иначе — "Доступ закрыт"."""

is_guest = False
is_admin = True
has_payment = True

if is_admin or (not is_guest and has_payment):
    print("Доступ открыт")
else:
    print("Доступ закрыт")